function getNestedValue(source, path) {
  return path.split('.').reduce((acc, key) => (acc && acc[key] !== undefined ? acc[key] : undefined), source)
}

function interpolate(template, vars = {}) {
  return String(template).replace(/\{(\w+)\}/g, (_, key) => (vars[key] ?? `{${key}}`))
}

export function createTranslator(locale = APP_LANGUAGES.EN) {
  const language = TRANSLATIONS[locale] || TRANSLATIONS[APP_LANGUAGES.EN]
  const fallback = TRANSLATIONS[APP_LANGUAGES.EN]

  return (path, vars = {}) => {
    const value = getNestedValue(language, path) ?? getNestedValue(fallback, path) ?? path
    return typeof value === 'string' ? interpolate(value, vars) : value
  }
}

export function formatDayCount(locale, count, compact = false) {
  if (compact) return `${count}`
  if (locale === APP_LANGUAGES.VI) return `${count} ngày`
  return `${count} ${count === 1 ? 'day' : 'days'}`
}

export function getModeLabel(locale, mode, variant = 'modes') {
  return createTranslator(locale)(`timer.${variant}.${mode}`)
}

export function getAchievementCopy(locale, achievementId) {
  const t = createTranslator(locale)
  return {
    name: t(`achievements.${achievementId}.name`),
    description: t(`achievements.${achievementId}.description`),
  }
}
