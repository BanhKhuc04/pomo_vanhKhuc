/**
 * Avatar Asset Pipeline
 * 
 * 📄 NOTE: For full specifications, guidelines, and asset MVP lists,
 * please read the AVATAR_ASSET_SPEC.md file in the project root.
 * 
 * HOW TO ADD AVATAR ASSETS:
 * 1. Place transparent PNG files in `public/images/avatar/`
 * 2. ALL layers must use the SAME canvas size (e.g., 256x256 or 512x512).
 * 3. The character must be aligned in the exact same position across all layers.
 * 4. Naming convention:
 *    - Base: `public/images/avatar/base/{animationState}.png`
 *      (e.g., idle.png, typing.png, relax.png, celebrate.png)
 *    - Hair: `public/images/avatar/hair/{hairStyle}-{hairColor}.png`
 *    - Glasses: `public/images/avatar/glasses/{glasses}.png`
 *    - Outfit: `public/images/avatar/outfit/{outfit}-{outfitColor}.png`
 *    - Accessory: `public/images/avatar/accessory/{accessory}.png`
 *    - Pets: `public/images/avatar/pets/{pet}-{animationState}.png` (optional)
 * 
 * If a PNG is missing or fails to load, the system will automatically 
 * fall back to the CSS-based layer rendering for that specific layer.
 * 
 * SPRITE / ANIMATION PREPARATION:
 * Currently, the system supports swapping the base asset based on `animationState`.
 * If you want to use sprite sheets in the future, you can update `AvatarCharacter.jsx`
 * to apply CSS step() animations to the img tags.
 */

/**
 * TEMPORARY ALIGNMENT TUNING
 * Used to manually align mismatched PNG layers generated by AI.
 * 
 * Ideally, all PNG layers should be exported from a single file by a designer 
 * with the exact same anchor point and canvas size. When standard assets are used,
 * reset all x, y to 0 and scale to 1.
 */
export const AVATAR_LAYER_TRANSFORMS = {
  base:      { x: 0, y: 0, scale: 1 },
  hair:      { x: 0, y: -20, scale: 1 }, // Example tweak for AI mismatched hair
  glasses:   { x: 0, y: -10, scale: 1.1 }, // Example tweak
  outfit:    { x: 0, y: 15, scale: 1.05 }, // Example tweak
  accessory: { x: 0, y: -30, scale: 1 },
  pets:      { x: 0, y: 0, scale: 1 },
}

export function getAvatarAssetPaths(config, animationState = 'idle') {
  if (!config) return {}
  
  const basePath = '/images/avatar'
  
  return {
    base: `${basePath}/base/${animationState}.png`,
    hair: `${basePath}/hair/${config.hairStyle}-${config.hairColor}.png`,
    glasses: config.glasses && config.glasses !== 'none' 
      ? `${basePath}/glasses/${config.glasses}.png` 
      : null,
    outfit: `${basePath}/outfit/${config.outfit}-${config.outfitColor}.png`,
    accessory: config.accessory && config.accessory !== 'none' 
      ? `${basePath}/accessory/${config.accessory}.png` 
      : null,
  }
}
